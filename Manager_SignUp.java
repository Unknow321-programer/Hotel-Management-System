    protected void signupActionPerformed(ActionEvent ae) {
        if(tFirstName.getText().equals("")) {
            JOptionPane.showMessageDialog(signup, "Empty First Name", "Invalid ", JOptionPane.WARNING_MESSAGE);
        }
        else if(tLastName.getText().equals("")) {
            JOptionPane.showMessageDialog(signup, "Empty Phone No.", "Invalid ", JOptionPane.WARNING_MESSAGE);
        }
        else if(temail.getText().equals("")) {
            JOptionPane.showMessageDialog(signup, "Empty Email", "Invalid ", JOptionPane.WARNING_MESSAGE);
        }
        else if(tpass1.getText().equals("") || tpass2.getText().equals("")) {
            JOptionPane.showMessageDialog(signup, "Empty Password !", "Invalid ", JOptionPane.WARNING_MESSAGE);
        }
        else if(email!=true || First!=true || Last!=true || passwword1!=true || confi!=true) {
            JOptionPane.showMessageDialog(signup, "Invalid Input's Format", "Check Details", JOptionPane.WARNING_MESSAGE);
        }
        else {
            try {
                Connection con = new dbConnection().getDbconnection();
                Statement stm = con.createStatement();
                String name = tFirstName.getText();
                
                String q = "select m_email from manager_details where m_email='"+temail.getText()+"'";
                ResultSet rs= stm.executeQuery(q);
                if(rs.next()!=true) {
                    q = "insert into manager_details(m_name,m_email,m_password,phone) values('"+name+"','"+temail.getText()+"','"+tpass1.getText()+"', "+tLastName.getText()+")";
                    int x = stm.executeUpdate(q);
                    if(x!=0) {
                        JOptionPane.showMessageDialog(signup, "Contact to Admin ", "Details Enetred", JOptionPane.WARNING_MESSAGE);
                        // dispose(); frame here 
                    }
                    else {
                        JOptionPane.showMessageDialog(signup, "Something Wrong When Storing Record", "Somthing Wrong", JOptionPane.WARNING_MESSAGE);
                    }
                }
                else {
                    JOptionPane.showMessageDialog(signup, "details are already Exits", "Somthing Wrong", JOptionPane.WARNING_MESSAGE);
                }
                con.close();
            }
            catch(Exception e) {
                System.out.println(e);
            }
        }
    }

