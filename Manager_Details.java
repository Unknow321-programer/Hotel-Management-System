    protected void RemoveActionPerformed(ActionEvent ae) {
        if(Manager_Table.getSelectedRow()!=-1) {
            try {
                String emails = Manager_Table.getValueAt(Manager_Table.getSelectedRow(), 3).toString();
                Connection con = new dbConnection().getDbconnection();
                Statement stm = con.createStatement();
                String q = "delete from manager_details where m_email='"+emails+"'";
                int x = stm.executeUpdate(q);
                if(x!=0) {
                    JOptionPane.showMessageDialog(null, "record deleted Fine", "Deleted", JOptionPane.PLAIN_MESSAGE);
                    DefaultTableModel dtm = (DefaultTableModel)Manager_Table.getModel();
                    dtm.removeRow(Manager_Table.getSelectedRow());
                }
                else {
                    JOptionPane.showMessageDialog(null, "Not record Deleted Fine", "Something Wrong", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                //TODO: handle exception
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Select Any One Row", "Not Selected", JOptionPane.WARNING_MESSAGE);
        }
    }

