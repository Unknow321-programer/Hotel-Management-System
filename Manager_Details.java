    protected void modelevent(TableModelEvent e) {
        int i = Manager_Table.getEditingRow();
        if(i>=0) {
            String stat =  Manager_Table.getValueAt(i, 4).toString();
            String id = Manager_Table.getValueAt(i, 0).toString();

            // System.out.println( Manager_Table.getValueAt(i, 4));
            try {
                Connection con = new dbConnection().getDbconnection();
                Statement stm = con.createStatement();
                String q = "update manager_details set status = "+stat+" where m_id = "+id+"";
                int x = stm.executeUpdate(q);
                if(x!=0) {
                    JOptionPane.showMessageDialog(null, "updated records of manager", "Fine", JOptionPane.PLAIN_MESSAGE);
                }
                else {
                    JOptionPane.showMessageDialog(null, "Not updated records of manager", "Fine", JOptionPane.WARNING_MESSAGE);
                }
            }
            catch(Exception a) {
                System.out.println(a);
            }
        }
    }

